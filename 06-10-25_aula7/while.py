# # # Laço pré-condicional - while
# inicio = 1 
# # fim = 4
# # while inicio <= fim:
# #     print(f"{inicio}")
# #     inicio = inicio + 1

valor_inicial = int(input("Valor Inicial: "))
valor_final = int(input("Valor Final: "))

while valor_inicial <= valor_final:
    print(valor_inicial)
    valor_inicial = valor_inicial + 1



