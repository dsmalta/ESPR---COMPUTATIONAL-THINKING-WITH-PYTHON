# Operadores aritméticos
"""
+ => Adição                         Prioridade 3
- => Subtração                      Prioridade 3
* => Multiplicação                  Prioridade 2
/ => Divisão                        Prioridade 2
% => Módulo (resto da divisão)      Prioridade 2
// => Divisão Inteira               Prioridade 2
** => Exponenciação                 Prioridade 1
"""
valor1 = 15
valor2 = 2
resp = valor1 + valor2
print(resp)
resp = valor1 - valor2
print(resp)
resp = valor1 * valor2
print(resp)
resp = valor1 / valor2
print(resp)
resp = valor1 % valor2
print(resp)
resp = valor1 // valor2
print(resp)

